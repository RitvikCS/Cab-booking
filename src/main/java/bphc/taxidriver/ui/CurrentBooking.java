    private void btnArrivedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArrivedActionPerformed
        currentBooking.setArrived(true);
        currentBooking.getDriver().setCurrentLocation(currentBooking.getDropOffLocation());
        currentBooking.getDriver().setBooked(false);
        
        // Ask customer for rating
        String val = JOptionPane.showInputDialog(null, "How would you rate this ride? (0-10) ");
        try {
            int rating = Integer.parseInt(val);
            currentBooking.getDriver().rate(rating);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error while parsing input. Make sure you've entered a valid number.");
            return;
        }
        
        if(currentBooking.save() && currentBooking.getDriver().save()) {
            Main.mainFrame.showCard("makeBooking"); // Let them make new booking now
        } else {
            JOptionPane.showMessageDialog(Main.mainFrame, "Failed to mark as arrived. Please try again.", "ERROR", 
                                          JOptionPane.ERROR);
        }
    }//GEN-LAST:event_btnArrivedActionPerformed


